#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*s2_;
	char	*str;

	s2_ = (char*)s2;
	str = s1;
	while (*str)
		str++;
	while (*s2_)
	{
		*str = *s2_;
		str++;
		s2_++;
	}
	*str = '\0';
	return (s1);
}
